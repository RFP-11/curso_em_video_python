# Crie um script python que leia dois números inteiros e mostre a soma entre eles.

a = int(input('Entre com um número inteiro: '))
b = int(input('Entre com outro número inteiro: '))
s = a + b
print('A soma destes valores é: {}.'.format(s))
print(f'A soma destes valores é: {s}.')
print(f'A soma destes valores é: {a + b}.')

